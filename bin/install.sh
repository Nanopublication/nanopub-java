#!/bin/bash
# Nanopub CLI installer
# Usage: curl -LsSf https://raw.githubusercontent.com/Nanopublication/nanopub-java/master/bin/install.sh | bash

set -e

INSTALL_DIR="${NANOPUB_INSTALL_DIR:-$HOME/.nanopub/bin}"
JAR_DIR="${NANOPUB_JAR_DIR:-$HOME/.nanopub/lib}"

# ── Helpers ────────────────────────────────────────────────────────────────────

info()    { printf '\033[1;34m[nanopub]\033[0m %s\n' "$*"; }
success() { printf '\033[1;32m[nanopub]\033[0m %s\n' "$*"; }
error()   { printf '\033[1;31m[nanopub]\033[0m %s\n' "$*" >&2; exit 1; }

need_cmd() {
  command -v "$1" &>/dev/null || error "Required command not found: $1"
}

# ── Preflight checks ──────────────────────────────────────────────────────────

need_cmd curl
need_cmd java

# ── Resolve latest version + direct download URL via GitHub API ───────────────

info "Resolving latest nanopub release..."

# The GitHub API returns JSON with a browser_download_url that is the final,
# redirect-free URL — avoids the BSD curl / multi-redirect issue on macOS.
API_RESPONSE=$(
  curl -fsSL \
    -H "Accept: application/vnd.github+json" \
    "https://api.github.com/repos/Nanopublication/nanopub-java/releases/latest"
) || error "Could not reach GitHub API. Check your internet connection."

# Extract version from tag_name (e.g. "nanopub-1.86.2" → "1.86.2")
VERSION=$(printf '%s' "$API_RESPONSE" \
  | grep '"tag_name"' \
  | head -1 \
  | sed 's/.*"tag_name": *"nanopub-//;s/".*//')

[ -z "$VERSION" ] && error "Could not determine latest version from GitHub API."

info "Latest version: $VERSION"

JAR_NAME="nanopub-${VERSION}-jar-with-dependencies.jar"

# Pull the browser_download_url for the jar asset directly — no redirects needed
JAR_URL=$(printf '%s' "$API_RESPONSE" \
  | grep '"browser_download_url"' \
  | grep "$JAR_NAME" \
  | head -1 \
  | sed 's/.*"browser_download_url": *"//;s/".*//')

[ -z "$JAR_URL" ] && error "Could not find download URL for $JAR_NAME in the release assets."

# ── Download jar ───────────────────────────────────────────────────────────────

JAR_PATH="$JAR_DIR/$JAR_NAME"
TMP_PATH="${JAR_PATH}.tmp"

mkdir -p "$JAR_DIR"

if [ -f "$JAR_PATH" ]; then
  info "Jar already present at $JAR_PATH — skipping download."
else
  info "Downloading $JAR_NAME..."

  # Download to a temp file first so a failed download never leaves a corrupt jar
  curl -fL --progress-bar --output "$TMP_PATH" "$JAR_URL" \
    || { rm -f "$TMP_PATH"; error "Download failed. URL: $JAR_URL"; }

  # Validate: a jar is a zip file — its first two bytes must be "PK"
  MAGIC=$(dd if="$TMP_PATH" bs=2 count=1 2>/dev/null | od -A n -t x1 | tr -d ' \n')
  if [ "$MAGIC" != "504b" ]; then
    rm -f "$TMP_PATH"
    error "Downloaded file is not a valid jar (bad magic bytes: $MAGIC). \
This usually means the download URL returned an HTML redirect page instead of the binary. \
Try running the installer again, or download manually from: $JAR_URL"
  fi

  mv "$TMP_PATH" "$JAR_PATH"
fi

# ── Write the np launcher script ──────────────────────────────────────────────

mkdir -p "$INSTALL_DIR"

NP_SCRIPT="$INSTALL_DIR/np"

cat > "$NP_SCRIPT" <<EOF
#!/bin/bash
# nanopub CLI launcher — auto-generated by install.sh
JAVAPARAMS="-Dsun.jnu.encoding=utf8 -Dfile.encoding=utf8"
exec java \$JAVAPARAMS -jar "$JAR_PATH" "\$@"
EOF

chmod +x "$NP_SCRIPT"

success "Installed np → $NP_SCRIPT"

# ── Shell PATH configuration ──────────────────────────────────────────────────

# Detect which shell config files to update
SHELL_CONFIGS=()
[ -f "$HOME/.bashrc"              ] && SHELL_CONFIGS+=("$HOME/.bashrc")
[ -f "$HOME/.bash_profile"        ] && SHELL_CONFIGS+=("$HOME/.bash_profile")
[ -f "$HOME/.zshrc"               ] && SHELL_CONFIGS+=("$HOME/.zshrc")
[ -f "$HOME/.config/fish/config.fish" ] && SHELL_CONFIGS+=("$HOME/.config/fish/config.fish")

PATH_LINE="export PATH=\"$INSTALL_DIR:\$PATH\""
FISH_LINE="fish_add_path \"$INSTALL_DIR\""

ADDED_TO=()
for cfg in "${SHELL_CONFIGS[@]}"; do
  if ! grep -qF "$INSTALL_DIR" "$cfg" 2>/dev/null; then
    if [[ "$cfg" == *.fish ]]; then
      echo "$FISH_LINE" >> "$cfg"
    else
      printf '\n# Added by nanopub installer\n%s\n' "$PATH_LINE" >> "$cfg"
    fi
    ADDED_TO+=("$cfg")
  fi
done

if [ "${#ADDED_TO[@]}" -gt 0 ]; then
  info "Added $INSTALL_DIR to PATH in: ${ADDED_TO[*]}"
else
  info "$INSTALL_DIR already present in PATH config."
fi

# ── Final instructions ────────────────────────────────────────────────────────

echo ""
success "nanopub $VERSION installed successfully!"
echo ""
echo "  To start using it, reload your shell:"
echo ""
echo "    source ~/.bashrc   # or ~/.zshrc, etc."
echo ""
echo "  Then run:"
echo ""
echo "    np check nanopubfile.trig"
echo ""
echo "  Tip: set NANOPUB_INSTALL_DIR or NANOPUB_JAR_DIR before running this"
echo "  script to customize installation paths."
echo ""
